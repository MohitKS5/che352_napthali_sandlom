function [ h ] = H_V(y, T_stage)
    Hv = 10^6.*[71.8 31.3 35.2 30.72 29.4];
    Tsat=[64.7 56 57.1 80.1 61.2];
    Tsat=Tsat+273.15;

    %methanol
    h(1) = y(1)*integral(@(T) 105800-362.23.*T+0.9379.*T.^2, 330, Tsat(1));
    h(1) = h(1) + Hv(1);
    h(1) = h(1) + integral(@(T)(0.39252*10^5+0.879.*10^5.*((1.9165.*(10^3)./T)./sinh(1.9165.*(10^3)./T)).^2+0.53654.*10^5.*((896.7./T)./sinh(896.7./T)).^2), Tsat(1), T_stage);
   
    %acetone
    h(2) = y(2)*integral(@(T) 135600-177.*T+0.2837.*T.^2+0.000689.*T.^3+0.000689.*T.^4, 330, Tsat(2));
    h(2) = h(2) + Hv(2);
    h(2) = h(2) + integral(@(T) (0.5704*10^5+1.632.*10^5.*((1.607.*(10^3)./T)./sinh(1.607.*10^3./T)).^2+0.968.*10^5.*((731.5./T)./sinh(731.5./T)).^2), Tsat(2), T_stage);
    
    %methyl acetate
    h(3) = y(3)*integral(@(T) 61260+270.9.*T, 330, Tsat(3));
    h(3) = h(3) + Hv(3);
    h(3) = h(3) + integral(@(T) (0.555*10^5+1.782.*10^5.*((1.26.*(10^3)./T)./sinh(1.26.*10^3./T)).^2+0.853.*10^5.*((562./T)./sinh(562./T)).^2),  Tsat(3), T_stage);
    
    %benzene
    h(4) = y(4)*integral(@(T) 162940-344.94.*T+0.85562.*T.^2, 330, Tsat(4));
    h(4) = h(4) + Hv(4);
    h(4) = h(4) + integral(@(T) (0.44767*10^5+2.3085.*10^5.*((1.4792.*(10^3)./T)./sinh(1.4792.*10^3./T)).^2+1.6836.*10^5.*((677.66./T)./sinh(677.66./T)).^2), Tsat(4), T_stage);
    
    %chloroform
    h(5) = y(5)*integral(@(T) 124850-166.34.*T+0.43209.*T.^2, 330, Tsat(5));
    h(5) = h(5) + Hv(5);
    h(5) = h(5) + integral(@(T) (0.3942*10^5+0.6573.*10^5.*((0.928.*(10^3)./T)./sinh(0.928.*10^3./T)).^2+0.493.*10^5.*((399.6./T)./sinh(399.6./T)).^2), Tsat(5), T_stage);
    h = sum(h);
    h = h + dep_V(y, T_stage)*1000;
end

